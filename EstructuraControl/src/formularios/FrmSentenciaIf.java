    private void jBtnEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEvaluarActionPerformed
        // TODO add your handling code here:
        try {
            int num = Integer.parseInt(this.jTfNum.getText());
            String resp = sIf.evaluarPosNeg(num);
            JOptionPane.showMessageDialog(this, resp,
                    "Evaluar Numero si es positivo o negativo",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (HeadlessException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),
                    "Error al calcular si el # es pos o neg",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnEvaluarActionPerformed

    private void jBtnEvaluarNumPosMult3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEvaluarNumPosMult3ActionPerformed
        // TODO add your handling code here:
        int num = Integer.parseInt(jTfNumero.getText());
        boolean resp = sIf.evaluarNumPosMult3(num);
        if(resp){
            this.jTfNumero.setBackground(Color.RED);
        }else{
            this.jTfNumero.setBackground(Color.WHITE);
        }     
    }//GEN-LAST:event_jBtnEvaluarNumPosMult3ActionPerformed

